package ex_09_Switch;

public class Lab_096_Interview {
    static void main(String[] args) {
        char code = 'C';
        switch (code){
            default ->
                System.out.println("Hello");
            case 'A' ->
                System.out.println("65");
            case 'B' ->
                System.out.println("66");

        }

    }
}
