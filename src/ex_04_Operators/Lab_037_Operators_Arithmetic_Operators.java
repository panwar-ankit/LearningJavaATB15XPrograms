package ex_04_Operators;

public class Lab_037_Operators_Arithmetic_Operators {

    static void main(String[] args) {
        // Arithmetic Operators
        // + (Addition)
        // - (Subtraction)
        // * (Multiplication)
        // / (Division)
        // % (Modulus) / Modulus - Remainder

        int a = 20;
        int b = 3;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        float c = 3.0f;
        System.out.println(a/c);

    }
}
